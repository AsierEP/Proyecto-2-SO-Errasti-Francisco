    public void actualizarDirectorios(){
        List direc = this.sim.getDirectorios();
        Node cabeza = direc.getPfirst();
        
        while(cabeza!=null){
            Directorio actu = (Directorio) cabeza.getDato();
            if (actu.getPadre()!=null){
                this.RemoveCB.addItem(actu.getNombre()+" ("+actu.getPadre()+")");
            }else{
                this.RemoveCB.addItem(actu.getNombre());
            }
            cabeza=cabeza.getPnext();
        }
    }

