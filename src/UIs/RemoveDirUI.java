    private void RemoveButtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RemoveButtActionPerformed
        String[] partes = this.RemoveCB.getSelectedItem().toString().split("\\(");
        String nombreDirectorio = partes[0].trim();
        String nombrePadre = partes[1].substring(0, partes[1].length() - 1).trim(); 
        this.sim.getDirectorios().mostrar();
        this.sim.eliminarDirectorio(nombreDirectorio, nombrePadre);
        this.sim.enable();
        this.setVisible(false);
    }//GEN-LAST:event_RemoveButtActionPerformed

