    private void actualizarDir(){
        List direc = this.sim.getDirectorios();
        Node cabeza = direc.getPlast();
        while(cabeza!=null){
            this.DirCB.addItem((String)cabeza.getDato());
            cabeza=cabeza.getPnext();
        }
    }
